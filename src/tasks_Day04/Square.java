package tasks_Day04;

public class Square {
    public static void main(String[] args) {

        double side = 10;

        double area = side * side;

        double perimeter = 4 * side;

        System.out.println("Square side = "+ side);
        System.out.println("Square area = "+ area);
        System.out.println("Square perimeter = "+ perimeter);

    }
}
